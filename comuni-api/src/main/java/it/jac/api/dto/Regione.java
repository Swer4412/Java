package it.jac.api.dto;

public class Regione {
	
	private String codRegione;
	private String denRegione;
	private String ripGeo;
	
	public String getCodRegione() {
		return codRegione;
	}
	public void setCodRegione(String codRegione) {
		this.codRegione = codRegione;
	}
	public String getDenRegione() {
		return denRegione;
	}
	public void setDenRegione(String denRegione) {
		this.denRegione = denRegione;
	}
	public String getRipGeo() {
		return ripGeo;
	}
	public void setRipGeo(String ripGeo) {
		this.ripGeo = ripGeo;
	}
	
}
