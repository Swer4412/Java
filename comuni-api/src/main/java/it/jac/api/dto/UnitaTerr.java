package it.jac.api.dto;

public class UnitaTerr {
	
	private String codUnitaTerr;
	private String codProv;
	private String denUnitaTerr;
	private String tipologiaUnitaTerr;
	private String regione;
	private String siglaAuto;
	
	public String getCodUnitaTerr() {
		return codUnitaTerr;
	}
	public void setCodUnitaTerr(String codUnitaTerr) {
		this.codUnitaTerr = codUnitaTerr;
	}
	public String getCodProv() {
		return codProv;
	}
	public void setCodProv(String codProv) {
		this.codProv = codProv;
	}
	public String getDenUnitaTerr() {
		return denUnitaTerr;
	}
	public void setDenUnitaTerr(String denUnitaTerr) {
		this.denUnitaTerr = denUnitaTerr;
	}
	public String getTipologiaUnitaTerr() {
		return tipologiaUnitaTerr;
	}
	public void setTipologiaUnitaTerr(String tipologiaUnitaTerr) {
		this.tipologiaUnitaTerr = tipologiaUnitaTerr;
	}
	public String getRegione() {
		return regione;
	}
	public void setRegione(String regione) {
		this.regione = regione;
	}
	public String getSiglaAuto() {
		return siglaAuto;
	}
	public void setSiglaAuto(String siglaAuto) {
		this.siglaAuto = siglaAuto;
	}
	
	
}	
